# Modulo operator will gives us remainder value
# Here num1 will be first input value
num1=float(input("enter First number: "))
# And num2 will be Second input value
num2=float(input("\nenter Second number: "))
# evaluation of two input values
value=num1%num2
# printing the value from the two inputs
print("the remainder  of {0} and {1} gives us {2}".format(num1,num2,value))